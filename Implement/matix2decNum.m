function decNum=matix2decNum(state)
decNum=state(1)*10^15+state(2)*10^14+state(3)*10^13+state(4)*10^12+ ...
    state(5)*10^11+state(6)*10^10+state(7)*10^9+state(8)*10^8+ ...
    state(9)*10^7+state(10)*10^6+state(11)*10^5+state(12)*10^4+ ...
    state(13)*10^3+state(14)*10^2+state(15)*10+state(16);
end